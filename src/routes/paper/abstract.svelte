<section id="abstract" aria-roledescription="Abstract">
  <h2 aria-roledescription="section title">
    <a class="anchor" name="abstract" href="#abstract">Abstract</a>
  </h2>
  <p aria-roledescription="paragraph">
    Data sonification—mapping data variables to auditory variables, such as
    pitch or volume—is used for data accessibility, scientific exploration, and
    data-driven art (e.g., museum exhibitions) among others. While a substantial
    amount of research has been made on effective and intuitive sonification
    design, software support is not commensurate, limiting researchers from
    fully exploring its capabilities. We contribute <em>Erie</em>, a declarative
    grammar for data sonification, that enables abstractly expressing auditory
    mappings. <em>Erie</em> supports specifying extensible <em>tone</em>
    designs (e.g., periodic wave, sampling, frequency/amplitude modulation synthesizers),
    various <em>encoding</em> channels, auditory legends, and
    <em>composition</em>
    options like sequencing and overlaying. Using standard Web Audio and Web Speech
    APIs, we provide an <em>Erie</em> compiler for web environments. We
    demonstrate the expressiveness and feasibility of <em>Erie</em> by
    replicating research prototypes presented by prior work and provide a
    sonification design gallery. We discuss future steps to extend <em>Erie</em>
    toward other audio computing environments and support interactive data sonification.
  </p>
